    @Override
    public void onSensorChanged(SensorEvent event) {
        fd.setIndeterminate(false);
        float x=event.values[0]*16.67f;
        float y=event.values[1]*16.67f;
        float z=event.values[2]*16.67f;
        int percentX = (int) x;
        if (percentX<0){
            rt.setProgress(Math.abs(percentX));
            lt.setProgress(0);
        }
        else if (percentX>0){
            lt.setProgress(Math.abs(percentX));
            rt.setProgress(0);
        }
        else{
            lt.setProgress(0);
            rt.setProgress(0);
        }
        int percentY = (int) y;
        if (percentY<0){
            fd.setProgress(Math.abs(percentY));
            bk.setProgress(0);
        }
        else if (percentY>0){
            bk.setProgress(Math.abs(percentY));
            fd.setProgress(0);
        }
        else{
            fd.setProgress(0);
            bk.setProgress(0);
        }

    }

